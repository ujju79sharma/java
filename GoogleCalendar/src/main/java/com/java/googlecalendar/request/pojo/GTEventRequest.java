package com.java.googlecalendar.request.pojo;

public class GTEventRequest {

	private String summary;
	private String location;
	private String description;
	private String startDate;
	private String startTime;
	private String endTime;
	private String endDate;
	private String timezone;

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getTimezone() {
		return timezone;
	}

	public void setTimezone(String timezone) {
		this.timezone = timezone;
	}

	@Override
	public String toString() {
		return "GTEvents [summary=" + summary + ", location=" + location + ", description=" + description
				+ ", startDate=" + startDate + ", startTime=" + startTime + ", endTime=" + endTime + ", endDate="
				+ endDate + "]";
	}
}